; | 1 2 3 |     |1 2 3|
; | 4 5 6 |  *  |4 5 6|
; | 7 8 9 |     |7 8 9|
; 1000 -> 1 2 3 4 5 6 7 8 9 ->FIRST MATRIX
; 1010 -> 1 2 3 4 5 6 7 8 9 ->SECOND MATRIX

ASSUME CS:CODE, DS:DATA
DATA SEGMENT
DATA ENDS

CODE SEGMENT
BEGIN:
		MOV AX, DATA
		MOV DS, AX

		MOV SI, 1000H   ;FROM 1000 FIRST MATRIX
		MOV BP, 00H     ;1010 SECOND MATRIX

		XOR AX, AX      ;1020 ASNSWER MATRIX
		XOR CX, CX

	L3:
		MOV DI, 1010H      ;SI= FIRST MAT ROW

	L1:                		;DI=SECOND MAT COL
		XOR BX, BX
		XOR DX, DX

	L2:
		MOV AL, [BX+SI]
		MOV DH, [DI]
		
		MUL DH
		ADD DL, AL
		XOR AX, AX
		
		MOV AL, 03H
		INC BL
		ADD DI, AX
		CMP DI, 1019H
		JB L2

		SUB DI, 0008H     ;A COLUMN COMPLETE, GO TO NEXT
		XOR BL, BL		  ;BL = 0
		PUSH SI           ;USE SI TO STORE ANS
		
		MOV SI, 1020H     ;STORE BEFORE SI VALUE IN STACK
		MOV BL, CL
		MOV DH, 00H
		MOV [BX+SI], DL   ;STORE ANS
		
		INC CL
		POP SI
		CMP DI, 1013H     ;A ROW COMPL
		JB L1

		MOV AL, 03H
		ADD SI, AX
		CMP SI, 1009H    ;TOTAL COMPLETED
		JB L3


	MOV AH, 4CH
	INT 21H

CODE ENDS
END BEGIN